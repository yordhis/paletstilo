

// ABRIR CARRO DE COMPRA
document.getElementById("abrir-carro-compra").addEventListener("click", function ocultar() {
    carroDeCompra = document.getElementById('carro-de-compra')
    carroDeCompra.style = "display:block;"

 
});


// CERRAR EL CARRO DE COMPRA
document.getElementById("cerrar-carro-compra").addEventListener("click", function ocultar() {
    carroDeCompra = document.getElementById('carro-de-compra')
    carroDeCompra.style = "display:none;"
 
});