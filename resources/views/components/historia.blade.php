            <h1 class="text-warning text-center bg-white">Historia</h1>
            <h2> Un poco de Historia: Pallet, Palet, Paleta o Palé</h2>
            <p>
                <strong>Origen</strong><br>
                Su origen data de 1924 en la que se patentó por primera vez por parte del estadounidense Howald
                T Hallowell que la llamó “Lift Truck Plattform” (Plataforma para subir en camiones) y quién utilizó la
                madera como material para su construcción debido a la facilidad de suministro y manejo de la misma.
                Más tarde y debido al desarrollo del transporte de mercancías, durante los años 30 se registraron otras dos patentes:
            </p>
            <ul>
                <li>
                    George Raymond (1938) registró una plataforma que complementaba al palet existente en la época.
                </li> 
                <li>
                    Carl Clark (1939) registró el tipo de palet que más se parece a lo que se conoce actualmente.
                </li>
            </ul>
            
            <p>
                En los años 40 se tiene constancia de otras dos patentes de palets los cuales puede que fuesen
                ideados antes pero que no se patentasen hasta entonces por causa de la guerra.
            </p>
            <ul>
                <li>
                    Robert Braun (1945) que patentó un palet de 4 entradas.
                    Norman Cahners (1949) que patentó un tipo de palet desechable.
                    Darling Graeme (1949) fue quien patentó el palet de cuatro entradas que más se asemeja al palet moderno que se utiliza actualmente.
                </li> 
                
            </ul>

            <p>
                Pero la difusión de los palets se la debemos al ejército para el suministro de armas, alimentos, etc. de sus tropas en Europa durante la 2ª guerra mundial.
            </p>

            <p>
                Respecto al palet Europeo, en 1950, con el impulso del ferrocarril, se hizo necesaria la normalización de un palet de dimensiones
                800x1200 mm que 18 redes de vías férreas europeas adaptaron con el nombre EUROPE (EUR).
            </p>

            <h2>¿Que es un Pallet?</h2>
            <p>
                Pallet, palet, paleta o palé son varios los términos para referirse a este soporte o plataforma de tablas para apilar la carga.
                Los palets son armazones rígidos sobre los que se coloca la mercancía distribuida de forma 
                homogénea en altura y superficie que abarcará sobre el pallet. Los pallets, 
                y en consecuencia la carga, serán transportados de un lado a otro del rack o la bodega mediante grúas horquilla o transpaletas.
            </p>

            <strong>Tipos de pallet</strong>
            <p>
                Existen diferentes clases de pallets, dependiendo de su tamaño, su composición, etc. Veamos cuáles son los principales pallets existentes y cuáles son sus características.
            </p>

            <strong>1) Pallets según sus dimensiones: </strong>
            <p>
                <b> Europaleta:</b> <br>
                Se trata de un soporte cuyas medidas son de 1200x800 mm. Es actualmente el más extendido en Europa. Este tipo de pallet está regulado por EPAL 
                (European Pallet Association, Asociación Europea de Palés) que según indica en su web, “alrededor de 500 millones están actualmente en circulación”. 
            </p>
            <p>
                Además, se trata de uno de los tipos de palés estandarizados por ISO (International Organization for Standardization, 
                Organización Internacional de Estandarización), la industria y la logística 
                europea, por tanto, utilizan el europallet como soporte principal para facilitar el flujo de 
                las mercancías y su agrupación en el proceso del transporte y el almacenaje. 
            </p>
            <p>
                Y es que, las medidas de la europaleta vienen determinadas por el ancho de los vagones, contenedores y
                 cajas de camiones, que son habitualmente de 2400 mm, lo que permite colocarlos de forma que aprovechen totalmente el espacio de carga.
            </p>

            <strong>Principales características:</strong>
            <ul>
                <li>
                    Medidas: longitud 800 mm, ancho 1200 mm.
                </li>
                <li>
                    Materiales: 11 tablas de madera, 9 tacos, 78 clavos.
                </li>
                <li>
                    Peso aproximado: 25 kg.
                </li>
                <li>
                    Carga de trabajo segura en movimiento: 1500 kg.
                </li>
                <li>
                    Carga estática: al apilar pallets cargados en una superficie sólida y uniforme, el palé más inferior no debe exceder una carga de más de 5500 kg.
                </li>
                <li>
                    Contienen varias marcas para su identificación, como el número de licencia o el método de tratamiento. No obstante, la más visible es la de las siglas EPAL.                
                </li>
            </ul>

