            <strong>2)Pallets según el número de entradas: </strong>
            <p>
                Las entradas se refieren al número de lados por donde la grúa horquilla elevadora introduce la horquilla para poder desplazar la carga.
            </p>
            <ul>
                <li>
                    De 4 entradas: la grúa horquilla o el medio mecánico para transportar el pallet puede acceder al palé por cualquier lado tanto del ancho como del largo.  
                </li>
                <li>
                    o	De 2 entradas: sólo se puede acceder al pallet por 2 lados entre sí enfrentados. 
                    Estos palés tienen menos movilidad que los de 4 entradas por el simple hecho de ser menos accesibles.
                </li>
            </ul>

